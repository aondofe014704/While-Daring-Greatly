"""Circle Area, Diameter and Circumference"""

"""Area"""

PI = 3.14159

radius = 2


area = PI * radius ** 2


print(f"The area of a circle is: {area}")



"""Diameter of a Circle"""

diameter = 2 * radius

print(f"The Diameter of the circle is: {diameter}")



"""Circumference"""


circumference = 2 * PI * radius

print(f"The Circimference of the Circle is: {circumference}")
