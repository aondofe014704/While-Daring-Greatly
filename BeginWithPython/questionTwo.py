"""(Arithmetic) for each of the following operators +, -, /, //, and **. Display the value of an expression with 27.5 as the left operand and 2 as the right operand."""

valueA = 27.5

valueB = 2

print(valueA + valueB)

print(valueA - valueB)

print(valueA * valueB)

print(valueA / valueB)

print(valueA // valueB)

print(valueA ** valueB)