public class Patterns{

public static void main(String[] args){


int mary = 5;

for(mary = 0; mary <= 5; mary++){
	for(int j =0; j <= 5; j++){
	System.out.print("4 ");
}
System.out.println();
}



int jack = 5;

for(jack = 0; jack <= 5; jack++){
	for(int j =0; j <= jack; j++){
	System.out.print("1 ");
}
System.out.println();
}


int songu = 5;

for(songu = 0; songu <= 5; songu++){
	for(int j = songu; j <= 5; j++){
	System.out.print("10 ");
}
System.out.println();
}

}


}