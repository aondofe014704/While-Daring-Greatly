public class NestedIfStatements{

public static void main(String[] args){


int Grade = 90;

if(Grade >= 90){
System.out.println("A");
}
else if(Grade >= 80){
System.out.println("B");
}
else if(Grade >= 70){
System.out.println("C");
}
else if(Grade >= 60){
System.out.println("D");
}
else if(Grade >= 50){
System.out.println("E");
}
else {
System.out.println("F");
}


}


}