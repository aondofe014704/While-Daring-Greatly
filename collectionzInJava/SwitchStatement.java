public class SwitchStatement{

public static void main(String[] args){

char switchChar = 'B';

switch(switchChar){
	case 'A':
		System.out.println("The char character is A");
		break;
	case 'B':
		System.out.println("The char character is B");
		break;
	case 'C':
		System.out.println("The char character is C");
		break;
	case 'D':
		System.out.println("The char character is D");
		break;
	case 'E':
		System.out.println("The char character is E");
		break;
	default:
		System.out.println("The char character value is not found");
}

}

}